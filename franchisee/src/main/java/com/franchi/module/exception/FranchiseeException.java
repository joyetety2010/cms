package com.franchi.module.exception;

public class FranchiseeException extends RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public FranchiseeException() {
		super();
	}

	public FranchiseeException(String message, Throwable cause) {
		super(message, cause);
	}

	public FranchiseeException(String message) {
		super(message);
	}

	public FranchiseeException(Throwable cause) {
		super(cause);
	}

	
}
